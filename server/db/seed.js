// для части пород добавляем отдельное предложение про еду если у них частый нюанс в источниках
function dogNutritionBreedExtra(name) {
  const n = String(name || "").toLowerCase();
  if (n.includes("далматин"))
    return "У далматинов нередко обсуждают риск мочекаменной болезни, тип корма и питьё по рекомендации ветеринара.";
  if (
    n.includes("мопс") ||
    n.includes("пекинес") ||
    n.includes("ши-тцу") ||
    n.includes("бостон-терьер") ||
    n.includes("гриффон") ||
    n.includes("пти-брабансон") ||
    n.includes("китайская хохлатая") ||
    n.includes("французский бульдог") ||
    (n.includes("бульдог") && n.includes("английский"))
  )
    return "Короткая морда: лишний вес сильно мешает дыханию, порцию и лакомства держат строго.";
  if (
    n.includes("грейхаунд") ||
    n.includes("уиппет") ||
    n.includes("борзая") ||
    n.includes("салуки") ||
    n.includes("фараонова")
  )
    return "Тонкое телосложение: вес и мышцы контролируют, резкую потерю веса с врачом не игнорируют.";
  if (
    n.includes("лабрадор") ||
    n.includes("золотистый") ||
    n.includes("бигль") ||
    n.includes("бассет") ||
    n.includes("такса") ||
    n.includes("корги") ||
    n.includes("кокер-спаниель") ||
    n.includes("спрингер-спаниель")
  )
    return "Склонны к перееданию: норму сверяют с весом и обхватом талии, не подкармливают со стола.";
  return "";
}

// собираем текст про корм из кусков по размеру активности и общие запреты в одну строку для базы
function dogNutrition(b) {
  const sizeLine =
    b.size === "крупный" ? "Крупные: лучше несколько небольших порций, щенку следят за ростом."
      : b.size === "средний" ? "Средние: порцию и лакомства сверяют с весом."
        : "Мелкие: мелкие гранулы, контроль веса и зубов.";
  const actLine =
    b.activity === "высокая" ? "Высокая нагрузка: калорий больше, норму всё равно ведут по весу."
      : b.activity === "низкая" ? "Мало двигается: калорий меньше, иначе ожирение." : "";
  const core =
    "Полнорационный корм по возрасту и размеру породы (сухой, влажный или оба), при проблемах с ЖКТ или кожей линию согласуют с врачом. Норма с упаковки, подправляют по весу. Вода всегда.";
  const tabu =
    "Еда со стола не заменяет корм. Не давать: шоколад, ксилитол, виноград и изюм, лук и чеснок, макадамии, алкоголь, сырое дрожжевое тесто, кости и сладости.";
  const vet = "При болезнях и аллергии рацион назначает ветеринар.";
  const extra = dogNutritionBreedExtra(b.name);
  return [core, sizeLine, actLine, extra, tabu, vet].filter(Boolean).join(" ");
}
