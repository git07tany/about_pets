const insertSql = `insert into small_pet_breeds (name, species, care_level, lifespan_min, lifespan_max, nutrition, care, health, image_url)
  values (?, ?, ?, ?, ?, ?, ?, ?, ?)`;

async function seed() {
  try {
    const db = await openDb();
    db.run("delete from small_pet_breeds");
    db.run("delete from sqlite_sequence where name='small_pet_breeds'");
    for (const b of breeds) {
      db.run(insertSql, [
        b.name,
        b.species,
        b.care_level,
        b.lifespan_min,
        b.lifespan_max,
        b.nutrition,
        b.care,
        b.health,
        b.image_url || null,
      ]);
    }
    save(db);
    console.log("Добавлено карточек мелких питомцев:", breeds.length);
  } catch (err) {
    console.error(err);
    process.exit(1);
  }
}

seed();
